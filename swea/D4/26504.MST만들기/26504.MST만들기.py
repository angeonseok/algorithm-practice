T = int(input())
for tc in range(1, T+1):
    n = int(input())
    n_lst = list(map(int, input().split()))

    n_lst.sort()
    min_mst = sum(n_lst[:n-1])

    max_mst = 0
    idx = 0
    jump = 1
    
    while idx < len(n_lst):
        max_mst += n_lst[idx]
        idx += jump
        jump += 1        

    print(min_mst, max_mst)